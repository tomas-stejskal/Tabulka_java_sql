    private void jTable1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MousePressed
        if (evt.getClickCount() == 2){
            //System.err.println("udalost");
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            int selectedRow = jTable1.getSelectedRow();
            this.selected_row = selectedRow;
            TableData data = new TableData();
            data.name = (String)model.getValueAt(selectedRow, 0);
            data.description = (String)model.getValueAt(selectedRow, 1);
            data.date = (Date)model.getValueAt(selectedRow, 2);
            data.status = (boolean)model.getValueAt(selectedRow, 3);
            
            EditRow er = new EditRow(this, rootPaneCheckingEnabled,data,this);
            er.setVisible(true);
        }
    }//GEN-LAST:event_jTable1MousePressed

