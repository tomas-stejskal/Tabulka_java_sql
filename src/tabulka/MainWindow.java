    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow > -1){
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int[] row = jTable1.getSelectedRows();
            for(int i=0;i<row.length;i++){
                model.removeRow(row[i]-i);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    public void editRow(TableData d){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setValueAt(d.name,selected_row,0);
        model.setValueAt(d.description,selected_row,1);
        model.setValueAt(d.date,selected_row,2);
        model.setValueAt(d.status,selected_row,3);
    }
    
    private void loadData(){
        SaveToSql sv = new SaveToSql();
        List<TableData> data = sv.loadData();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        for (int i=0;i<data.size();i++){
            Object[] d = {data.get(i).name,data.get(i).description,data.get(i).date,data.get(i).status};
            model.addRow(d);
        }
    }
